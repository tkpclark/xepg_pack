int CRC32_Byte(int preg,int x)
{
    int i;

    for(i = 0, x <<= 24; i < 8; i++, x <<= 1)
    {
        preg = (preg << 1) ^ (((x ^ preg) >> 31) & 0x04C11DB7);
    }
    return (preg);
}
int CRC32(char *buf,int size)
{
    int crc = 0xffffffffL;
    int i;

    for(i = 0; i < size; i++)
    {
        crc = CRC32_Byte(crc, *buf++);
    }
    return (crc);
}
